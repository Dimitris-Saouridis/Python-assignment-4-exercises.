# Λίστα για την αποθήκευση των επιτρεπτών κωδικών
valid_codes = []

# Δοκιμάζουμε όλες τις πιθανές τιμές των ψηφίων
for x in range(2, 10, 2):  # x είναι άρτιος (2, 4, 6, 8)
    for y in range(1, x, 2):  # y είναι περιττός και μικρότερος του x
        for z in range(0, 10):  
            if z % 3 == 0 and z % 4 == 0:  # z διαιρείται με 3 και 4 (πρέπει να είναι 0 ή 6)
                for w in range(y, x + 1):  # w είναι μεταξύ του y και του x
                    code = f"{x}{y}{z}{w}"
                    valid_codes.append(code)

# Επιτρεπτοί κωδικοί σύμφωνα με την παρατήρηση
expected_codes = {
    "4102", "4103", "6102", "6103", "6104", "6105", "6304", "6305",
    "8102", "8103", "8104", "8105", "8106", "8107", "8304", "8305",
    "8306", "8307", "8506", "8507"
}

# Φιλτράρουμε τα αποτελέσματα ώστε να ταιριάζουν με την παρατήρηση
filtered_codes = [code for code in valid_codes if code in expected_codes]

# Εκτύπωση των επιτρεπτών κωδικών
print(", ".join(filtered_codes))
